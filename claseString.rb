=begin
Los métodos reverse, capitalize y length son métodos de instancia de la clase String
que se utilizan para realizar diferentes operaciones en una cadena.
=end

=begin
reverse: Este método invierte el orden de los caracteres de una cadena
y devuelve la cadena resultante.
=end

str = "Hola"
puts str.reverse

=begin
En este ejemplo, el método reverse invierte el orden de los caracteres de la cadena str,
produciendo la cadena resultante "aloH".
=end

=begin
capitalize: Este método convierte el primer carácter de una cadena en mayúscula y
el resto de los caracteres en minúscula, y devuelve la cadena resultante.
=end

str = "hola mundo"
puts str.capitalize

=begin
En este ejemplo, el método capitalize convierte el primer carácter de la cadena str en mayúscula
y el resto en minúscula, produciendo la cadena resultante "Hola, mundo".
=end

=begin
length: Este método devuelve la longitud (número de caracteres) de una cadena.
=end

str = "Hola mundo"
puts str.length

=begin
En este ejemplo, el método length devuelve el número de caracteres de la cadena str, que es 10.
=end
